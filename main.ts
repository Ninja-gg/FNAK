function get_vent_direction (guy: Sprite) {
    vx = guy.vx
    vy = guy.vy
    if (Math.abs(vx) < 0.001 && Math.abs(vy) < 0.001) {
        return 0
    }
    if (Math.abs(vx) > Math.abs(vy)) {
        if (vx < 0) {
            return 1
        } else {
            return 2
        }
    } else {
        if (vy < 0) {
            return 3
        } else {
            return 4
        }
    }
}
